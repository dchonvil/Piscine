char	*ft_strcat(char *dest, char *src)
{
	int c;
	int i;

	c = 0;
	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[c] != '\0')
	{
		dest[i] = src[c];
		c++;
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
