int	ft_is_prime(int nb)
{
	int i;
	int n;

	if (nb < 2)
		return (0);
	i = 2;
	n = 1;
	while (i <= (nb / i))
	{
		if (nb % i == 0)
		{
			n = 0;
			return (n);
		}
		i++;
	}
	return (n);
}
